import mongoose from 'mongoose';
const gamesSchema = new mongoose.Schema({
    img:{type:String,required:true},
    detailImg1:{type:String},
    detailImg2:{type:String},
    name: {type:String,unique:[true,"This game has alredy exist"],required:true},
    heading:{type:String},
    price:{type:String,required:true},
    rating:{type:String},
    developer:{type:String},
    publisher:{type:String},
    releaseDate:{type:String},
    minSystem:{type:Object},
    recSystem:{type:Object},
    h1:{type:String},
    h2:{type:String},
    h3:{type:String},
    h1Des:{type:String},
    h2Des:{type:String},
    h3Des:{type:String},
    showMore:{type:Object},
    privacy:{type:String},
    discount:{type:Boolean,default:false},
    discountPercent:{type:String,default:"-0%"},
    topSellers:{type:Boolean,default:false},
    mostPlayed:{type:Boolean,default:false},
    dealsOfWeek:{type:Boolean,default:false},
  });
 export const GamesModel = mongoose.model('GamesModel', gamesSchema);